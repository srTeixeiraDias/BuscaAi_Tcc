  <h5>O inicio</h5>

Em meio a uma pandemia mundial no ano de 2020, pequenos comércios que não conseguiam pagar uma plataforma de vendas ou um sistema proprio de venda
, foram os mais afetados tendo ate que baixar as portas. O BuscaAi foi projetado para atender o seu estabelecimento que precisa de ajuda para alavancar suas vendas, 
conseguindo divulgar sua loja e seus produtos de maneira fácil através do nosso site e aplicativo para Android e IOS, facilitando o acesso do consumidor ao item desejado.
<p></p>
<h5>Missão</h5>
<p>Chegar a onde nenhuma outra empresa chegou e revolucionar o mercado com um aplicativo básico de vendas e compras para todos os tipos de produtos</p>

<h5>Visão</h5>
<p>Nossa empresa preza muito os nossos consumidores e por isso desenvolvemos um aplicativo com uma interface de fácil acesso para todos os tipos de pessoas. Achamos que desse jeito podemos chamar ainda mais pessoas para o nosso aplicativo.</p>

<h5>Valores</h5>

    <p> Priorizamos suporte ao cliente e sempre tentamos ajudar em suas dúvidas ou dificuldades para usar o nosso app.
